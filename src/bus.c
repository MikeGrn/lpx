uint32_t toBigEndian(uint32_t littleEndian) {
    return ((littleEndian >> 24) & 0xff) |
           ((littleEndian<<8)&0xff0000) |
           ((littleEndian>>8)&0xff00) |
           ((littleEndian<<24)&0xff000000);
}

